package Assignment;

public class DataType {
    public static void main(String[] args) {
        String s = "Ashutosh"; // String type
        int age = 24; // Integer type
        char gen = 'M';
        boolean rs = false;

        float f = 6.7f; // float type
        System.out.println("I am : " + s);
        System.out.println("My Age: " + age);
        System.out.println("My gender: " + gen);
        System.out.println("Relationshp status " + rs);
        System.out.println("Float Number : " + f);

    }

}
